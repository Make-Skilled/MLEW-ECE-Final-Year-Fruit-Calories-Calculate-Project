def get_fruit_nutrition(fruit_name):
    """Get nutritional information for a specific fruit."""
    return fruit_nutrition_data.get(fruit_name.upper(), None)

def calculate_serving_calories(fruit_name, weight_grams):
    """Calculate calories for a specific serving size."""
    fruit_data = get_fruit_nutrition(fruit_name)
    if fruit_data:
        return (fruit_data["calories"] * weight_grams) / fruit_data["serving_size"]
    return None

def get_seasonal_fruits():
    """Get fruits that are in season."""
    current_season = get_current_season()
    return [fruit for fruit, data in fruit_nutrition_data.items() 
            if current_season.lower() in data["season"].lower()]

def get_current_season():
    """Helper function to determine current season."""
    from datetime import datetime
    month = datetime.now().month
    if month in [3, 4, 5]:
        return "Spring"
    elif month in [6, 7, 8]:
        return "Summer"
    elif month in [9, 10, 11]:
        return "Fall"
    else:
        return "Winter"
